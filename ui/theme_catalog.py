def _preset(
    description: str,
    *,
    category: str | None = None,
    colors: Dict[str, Any],
    glass: Dict[str, Any] | None = None,
    background: Dict[str, Any] | None = None,
    player_chrome: Dict[str, Any] | None = None,
    home_chrome: Dict[str, Any] | None = None,
    experience: Dict[str, Any] | None = None,
    app_customization: Dict[str, Any] | None = None,
) -> Dict[str, Any]:
    result = _deep_merge(
        _BASE_PRESET,
        {
            "description": description,
            "colors": colors,
            "glass": glass or {},
            "background": background or {},
            "player_chrome": player_chrome or {},
            "home_chrome": home_chrome or {},
            "experience": experience or {},
            "app_customization": app_customization or {},
        },
    )
    if category is not None:
        result["category"] = category
    return result


